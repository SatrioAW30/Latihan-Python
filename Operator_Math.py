x = 10
y = 5

penjumlahan = x+y #operator penjumlahan
pengurangan = x-y #operator pengurangan 
perkalian = x*y #operator perkalian
pembagian = x/y #operator pembagian
modulus = x%y #operator yang menampilkan hasil dari sisa bagi
pemangkatan = x**y #operator untuk memangkatkan
pembagian_bulat = x//y #operator yang menampilkan hasil pembagian dalam bilangan bulat ("tidak akan menampilakan angka di belakang koma")

print(penjumlahan)
print(pengurangan)
print(perkalian)
print(pembagian)
print(modulus)
print(pemangkatan)
print(pembagian_bulat)